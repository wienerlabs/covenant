const scenes: Record<Scene, string[]> = {
  idle: idleFrames,
  escrow: escrowFrames,
  proof: proofFrames,
  handshake: handshakeFrames,
};

export default function AsciiAnimation({
  scene,
  width = "280px",
  height = "130px",
}: AsciiAnimationProps) {
  const [frameIndex, setFrameIndex] = useState(0);
  const frames = scenes[scene];

  useEffect(() => {
    setFrameIndex(0);
  }, [scene]);

  useEffect(() => {
    const interval = setInterval(() => {
      setFrameIndex((prev) => (prev + 1) % frames.length);
    }, 180);
    return () => clearInterval(interval);
  }, [frames]);

  return (
    <div
      style={{
        width,
        height,
        border: "1px solid #000000",
        borderRadius: "8px",
        backgroundColor: "#fafafa",
        padding: "12px",
        fontFamily: "inherit",
        fontSize: "10px",
        lineHeight: 1.3,
        whiteSpace: "pre",
        overflow: "hidden",
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
        color: "#333",
      }}
    >
      {frames[frameIndex]}
    </div>
  );
}
