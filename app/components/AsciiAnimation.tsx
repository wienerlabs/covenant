const scenes: Record<Scene, string[]> = {
  idle: idleFrames,
  escrow: escrowFrames,
  proof: proofFrames,
  handshake: handshakeFrames,
  mint: mintFrames,
};

export default function AsciiAnimation({
  scene,
  width = "280px",
  height = "130px",
  variant = "light",
}: AsciiAnimationProps) {
  const [frameIndex, setFrameIndex] = useState(0);
  const frames = scenes[scene];

  useEffect(() => {
    setFrameIndex(0);
  }, [scene]);

  useEffect(() => {
    const interval = setInterval(() => {
      setFrameIndex((prev) => (prev + 1) % frames.length);
    }, 180);
    return () => clearInterval(interval);
  }, [frames]);

  return (
    <div
      style={{
        width,
        height,
        border: variant === "dark"
          ? "1px solid rgba(255,255,255,0.2)"
          : "1px solid #000000",
        borderRadius: "8px",
        backgroundColor: variant === "dark"
          ? "rgba(0,0,0,0.35)"
          : "#fafafa",
        backdropFilter: variant === "dark" ? "blur(12px)" : "none",
        padding: "12px",
        fontFamily: "inherit",
        fontSize: "10px",
        lineHeight: 1.3,
        whiteSpace: "pre",
        overflow: "hidden",
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
        color: variant === "dark"
          ? "rgba(255,255,255,0.75)"
          : "#333",
      }}
    >
      {frames[frameIndex]}
    </div>
  );
}
